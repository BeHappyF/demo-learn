# for..in..
d = {'a': 1, 'b': 2, 'c': 3}
for k in d:
    print(k)    # a b c

for v in d.values():
    print(v)    # 1 2 3

for k, v in d.items():
    print(k, ':', v)
# a : 1
# b : 2
# c : 3
